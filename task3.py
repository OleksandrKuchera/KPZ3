# -*- coding: utf-8 -*-
kyt1 = float(input("Перший кут трикутника (в градусах): "))
kyt2 = float(input("Другий кут трикутника (в градусах): "))

if kyt1 + kyt2 < 180:
    print("Такий трикутник існує.")
    if kyt1 == 90 or kyt2 == 90 or (kyt1 + kyt2 == 90):
        print("Трикутник є прямокутним.")
    else:
        print("Трикутник не є прямокутним.")
else:
    print("Трикутник не існує.")